<?php
// احصل على اسم الملف
$file = "omerosta.txt";

// احصل على البيانات من طلب POST
$data = $_POST["html"];

// تحقق إذا كانت البيانات غير فارغة
if ($data != "") {
    // اكتب البيانات إلى الملف مع إضافتها إلى نهايته
    file_put_contents($file, $data, FILE_APPEND);

    // اعرض رسالة نجاح
    echo "تمت الإضافة بنجاح";
}
else {
    // اعرض رسالة خطأ
    echo "لا يوجد بيانات للإضافة";
}
?>
